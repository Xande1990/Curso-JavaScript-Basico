// funcao: 2 params -> console.log(+);
function somar(n1, n2) {
    console.log(n1 + n2);
}
// funcao: 2 params -> console.log(-);
function subtrair(a, b) {
    console.log(a - b);
}

// ...(3, 4)
somar(3, 4);    
// ...(31, 98)
somar(31, 98);


// ...(11, 7)
subtrair(11, 7);
// ...(19, 52)
subtrair(19, 52);

